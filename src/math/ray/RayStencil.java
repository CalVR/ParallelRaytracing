package math.ray;

public abstract class RayStencil extends Ray {
	
	/*
	 * An extension of a ray that provides a stenciling interface
	 */
	
	/* *********************************************************************************************
	 * Static Vars
	 * *********************************************************************************************/
	private static final long serialVersionUID = 5052453050506677189L;
	

	/* *********************************************************************************************
	 * Instance Vars
	 * *********************************************************************************************/
	//


	/* *********************************************************************************************
	 * Constructors
	 * *********************************************************************************************/
	public RayStencil()
	{
		super();
	}

	
	/* *********************************************************************************************
	 * Abstract Methods
	 * *********************************************************************************************/
	public abstract Iterable<Ray> stencil(Ray primaryRay);
}
