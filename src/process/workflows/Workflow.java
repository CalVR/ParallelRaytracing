package process.workflows;

import process.Job;

public abstract class Workflow extends Job {
	
	/*
	 * A base class for composite jobs
	 */
	

	/* *********************************************************************************************
	 * Static Vars
	 * *********************************************************************************************/
	private static long nextWorkflowID;
	

	/* *********************************************************************************************
	 * Static Constructor
	 * *********************************************************************************************/
	static
	{
		nextWorkflowID = 0;
		idPrefix = "W";
	}
	

	/* *********************************************************************************************
	 * Static Helper Methods
	 * *********************************************************************************************/
	private static synchronized long nextWorkflowID() { return nextWorkflowID++; }
	
	
	/* *********************************************************************************************
	 * Vars
	 * *********************************************************************************************/
	private long workflowID = nextWorkflowID();

	
	/* *********************************************************************************************
	 * Constructors
	 * *********************************************************************************************/


	/* *********************************************************************************************
	 * Concrete Methods
	 * *********************************************************************************************/
	public long getWorkflowID() { return workflowID; }
	
	
	/* *********************************************************************************************
	 * Abstract Methods
	 * *********************************************************************************************/


}
