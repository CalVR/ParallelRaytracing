package file.sslevel;

import java.util.ArrayList;

public class SslLevelData {
	
	/*
	 * A storage object for data loaded from an .ssl file
	 */

	/* *********************************************************************************************
	 * Instance Vars
	 * *********************************************************************************************/
	protected ArrayList<Gate> gates;
	

	/* *********************************************************************************************
	 * Constructor
	 * *********************************************************************************************/
	public SslLevelData()
	{
		gates = new ArrayList<Gate>();
	}


	/* *********************************************************************************************
	 * Getters
	 * *********************************************************************************************/
	public ArrayList<Gate> getGates() {
		return gates;
	}
	

	/* *********************************************************************************************
	 * Adders
	 * *********************************************************************************************/
	public void addGate(Gate g)
	{
		gates.add(g);
	}

}
