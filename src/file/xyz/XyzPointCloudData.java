package file.xyz;

import java.util.ArrayList;

import raytrace.geometry.Vertex;

public class XyzPointCloudData {
	
	/*
	 * A storage object for data loaded from an .xyz file
	 */

	/* *********************************************************************************************
	 * Instance Vars
	 * *********************************************************************************************/
	protected ArrayList<Vertex> points;
	

	/* *********************************************************************************************
	 * Constructor
	 * *********************************************************************************************/
	public XyzPointCloudData()
	{
		points = new ArrayList<Vertex>();
	}


	/* *********************************************************************************************
	 * Getters
	 * *********************************************************************************************/
	public ArrayList<Vertex> getPoints() {
		return points;
	}
	

	/* *********************************************************************************************
	 * Adders
	 * *********************************************************************************************/
	public void addPoint(Vertex v)
	{
		points.add(v);
	}

}
