package raster.pixel;

import raster.Pixel;
import raytrace.color.Color;

public class Gamma18PT extends PixelTransform {
	
	/*
	 * Applies a gamma value of 1.8
	 */
	
	/* *********************************************************************************************
	 * Instance Vars
	 * *********************************************************************************************/
	//
	

	/* *********************************************************************************************
	 * Constructor
	 * *********************************************************************************************/
	public Gamma18PT()
	{
		//
	}
	

	/* *********************************************************************************************
	 * Transform Override
	 * *********************************************************************************************/
	@Override
	public Color transform(Pixel pixel)
	{
		Color result = pixel.getColor().gammaEncode(1.8);
		
		return result;
	}
	
}