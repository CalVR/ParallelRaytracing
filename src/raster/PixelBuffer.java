package raster;

public class PixelBuffer {
	

	/* *********************************************************************************************
	 * Constructor
	 * *********************************************************************************************/
	protected int[] buffer;

	
	/* *********************************************************************************************
	 * Constructor
	 * *********************************************************************************************/
	public PixelBuffer(int width, int height)
	{
		buffer = new int[width * height];
	}
	

	/* *********************************************************************************************
	 * Getter
	 * *********************************************************************************************/
	public int[] getPixels() { return buffer; }
}
